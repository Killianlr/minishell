int	clear_terminal(void)
{
	char		*clear;
	char		*args[2];
	extern char	**__environ;
	int			pid;

	clear = "/usr/bin/clear";
	args[0] = clear;
	args[1] = NULL;
	pid = fork();
	if (pid == 0)
	{
		if (!__environ[0])
			return (1);
		if (access(clear, X_OK) == 0)
			execve(clear, args, __environ);
		return (1);
	}
	else if (pid > 0)
	{
		waitpid(pid, NULL, 0);
		display_minishell();
	}
	return (0);
}
